package mgs_lecture.ch18;

public class LambdaProduct {
    int id;
    String name;
    int price;

    public LambdaProduct(int id, String name, int price) {
        this.id = id;
        this.name = name;
        this.price = price;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public int getPrice() {
        return price;
    }
}