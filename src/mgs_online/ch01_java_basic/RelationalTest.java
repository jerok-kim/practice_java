package mgs_online.ch01_java_basic;

public class RelationalTest {
    public static void main(String[] args) {
        int num1 = 5;
        int num2 = 3;

        boolean value = (num1 > num2);
        System.out.println(value);

        System.out.println(num1 < num2);
        System.out.println(num1 >= num2);
        System.out.println(num1 <= num2);
        System.out.println(num1 == num2);
        System.out.println(num1 != num2);
    }
}
